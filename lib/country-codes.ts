/** Default country code (US/Canada). */
export const DEFAULT_COUNTRY_CODE = "1"

/** Parse E.164 into country code and national number (best-effort). */
export function parseE164(e164: string | null | undefined): { countryCode: string; national: string } {
  if (!e164 || typeof e164 !== "string") {
    return { countryCode: DEFAULT_COUNTRY_CODE, national: "" }
  }
  const digits = e164.replace(/\D/g, "")
  if (digits.length === 0) return { countryCode: DEFAULT_COUNTRY_CODE, national: "" }
  // Try to match longest dial code first (e.g. 1 then 44, 91, 358...)
  const sorted = [...COUNTRY_DIAL_CODES].sort((a, b) => b.code.replace(/\D/g, "").length - a.code.replace(/\D/g, "").length)
  for (const { code } of sorted) {
    const codeDigits = code.replace(/\D/g, "")
    if (digits.startsWith(codeDigits)) {
      const national = digits.slice(codeDigits.length).replace(/^0+/, "") || ""
      return { countryCode: code, national }
    }
  }
  // Fallback: assume +1 if 10 or 11 digits
  if (digits.length === 10 || (digits.length === 11 && digits.startsWith("1"))) {
    const national = digits.length === 11 ? digits.slice(1) : digits
    return { countryCode: "1", national }
  }
  return { countryCode: DEFAULT_COUNTRY_CODE, national: digits }
}

/** Build E.164 from country code and national digits. */
export function toE164(countryCode: string, national: string): string {
  const d = national.replace(/\D/g, "")
  if (!d) return ""
  const code = countryCode.replace(/\D/g, "")
  return `+${code}${d}`
}
